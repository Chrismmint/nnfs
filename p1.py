# a basic neuron with 3 neurons connected to it - the output is the
# sum of the input from each of the neurons multiplied by that inputs
# weight, plus whatever bias the neuron has.

inputs = [1.2, 5.1, 2.1]
weights = [2.1, 3.1, 8.7]
bias = 3

output = inputs[0]*weights[0] + inputs[0]*weights[0] + inputs[0]*weights[0] + bias

print(output)
